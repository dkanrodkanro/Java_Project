// 유튜버 생활코딩의 Java 입문 수업 재생목록에서 36번 영상 조건문(2/4): else
package basic.org.opentutorials.javatutorials.condition;

public class Condition3Demo {

    public static void main(String[] args) {

        if(true){   // if문의 조건절이 true라면 if 뒤에 따라오는 {}안의 내용만 실행 그 후 if문 전체를 탈출한다.

            System.out.println(1);

        } else {    // if문의 조건절이 false라면 else 뒤에 따라오는 {}안의 내용만 실행 그 후 if문 전체를 탈출한다.

            System.out.println(2);

        }

    }

}
